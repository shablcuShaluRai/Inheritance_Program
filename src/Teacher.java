public class Teacher {
	int id;
	String name;
	String address;
	float sal;

	void setId(int id) {
		this.id = id;
	}

	int getId() {
		return id;
	}

	void setName(String name) {
		this.name = name;
	}

	String getName() {
		return name;
	}

	void setAddres(String address) {
		this.address = address;
	}

	String getAdress() {
		return address;
	}

	void setSal(float sal) {
		this.sal = sal;
	}

	float getsal() {
		return sal;
	}
}

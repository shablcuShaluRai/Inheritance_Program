
public class Student {
	int id;
	String name;
	String address;
	int mark;

	void setId(int id) {
		this.id = id;
	}

	int getId() {
		return id;
	}

	void setName(String name) {
		this.name = name;
	}

	String getName() {
		return name;
	}

	void setAddres(String address) {
		this.address = address;
	}

	String getAdress() {
		return address;
	}

	void setMark(int mark) {
		this.mark = mark;
	}

	int getmark() {
		return mark;
	}

}
